CREATE TABLE `users` (
  `USER_GUID` varchar(64) NOT NULL,
  `USER_NAME` varchar(120) NOT NULL,
  `PASSWORD` varchar(100) NOT NULL,
  `FIRST_NAME` varchar(256) NOT NULL DEFAULT '',
  `LAST_NAME` varchar(256) DEFAULT NULL,
  `GENDER` varchar(6) DEFAULT NULL,
  `USER_TYPE` varchar(20) NOT NULL,
  `STATUS` varchar(6) DEFAULT NULL,
  `TITLE` varchar(20) NOT NULL DEFAULT 'Mr',
  `TENANT_ID` int(11) NOT NULL,
  `CREATED_BY` varchar(64) DEFAULT NULL,
  `CREATED_DATE` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `EMAIL` varchar(120) DEFAULT NULL,
  `TELEPHONE` varchar(25) DEFAULT NULL,
  PRIMARY KEY (`USER_GUID`),
  UNIQUE KEY `UNQ_USERNAME_TENANT` (`USER_NAME`,`TENANT_ID`),
  KEY `FK_USER_TENANT` (`TENANT_ID`),
  KEY `IND001_USERNAME` (`USER_NAME`) USING BTREE,
  FULLTEXT KEY `USER_NAME_FULLTEXT` (`USER_NAME`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;